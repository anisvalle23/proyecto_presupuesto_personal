-- 4) UPDATE
CREATE OR ALTER PROCEDURE SP_ACTUALIZAR_OBLIGACION_FIJA(
  P_ID_OBLIGACION            INTEGER,
  P_ID_SUBCATEGORIA          INTEGER,
  P_NOMBRE_OBLIGACION        VARCHAR(100),
  P_DESCRIPCION_OBLIGACION   VARCHAR(200),
  P_MONTO_FIJO_MENSUAL       DECIMAL(18,2),
  P_DIA_VENCIMIENTO          INTEGER,
  P_VIGENTE                  BOOLEAN,
  P_FECHA_INICIO             DATE,
  P_FECHA_FIN                DATE,
  P_MODIFICADO_POR           INTEGER
)
AS
BEGIN
  UPDATE OBLIGACION_FIJA O
  SET
    O.ID_SUBCATEGORIA        = :P_ID_SUBCATEGORIA,
    O.NOMBRE_OBLIGACION      = :P_NOMBRE_OBLIGACION,
    O.DESCRIPCION_OBLIGACION = :P_DESCRIPCION_OBLIGACION,
    O.MONTO_FIJO_MENSUAL     = :P_MONTO_FIJO_MENSUAL,
    O.DIA_VENCIMIENTO        = :P_DIA_VENCIMIENTO,
    O.VIGENTE                = :P_VIGENTE,
    O.FECHA_INICIO           = :P_FECHA_INICIO,
    O.FECHA_FIN              = :P_FECHA_FIN,
    O.MODIFICADO_POR         = :P_MODIFICADO_POR
  WHERE O.ID_OBLIGACION = :P_ID_OBLIGACION;
END^

-- 5) DELETE 
CREATE OR ALTER PROCEDURE SP_ELIMINAR_OBLIGACION_FIJA(
  P_ID_OBLIGACION   INTEGER,
  P_MODIFICADO_POR  INTEGER
)
AS
BEGIN
  UPDATE OBLIGACION_FIJA O
  SET
    O.VIGENTE        = FALSE,
    O.MODIFICADO_POR = :P_MODIFICADO_POR
  WHERE O.ID_OBLIGACION = :P_ID_OBLIGACION;
END^

